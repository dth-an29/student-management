    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try {
            clsData.OpenConnect();
            PreparedStatement stm = clsData.con.prepareStatement("Select distinct NamHoc from GiangDay where MaGV=?");
            stm.setString(1, maGV);
            ResultSet rs = stm.executeQuery();
            while (rs.next()) {
                cboNamHoc.addItem(rs.getString(1));
            }

            PreparedStatement stm1 = clsData.con.prepareStatement("Select distinct HocKy from GiangDay where MaGV=?");
            stm1.setString(1, maGV);
            ResultSet rs1 = stm1.executeQuery();
            while (rs1.next()) {
                cboHocKy.addItem(rs1.getString(1));
            }
            clsData.CloseConnect();
        } catch (SQLException ex) {
            Logger.getLogger(frmMainGV.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formWindowOpened

    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        // TODO add your handling code here:
        frmCapNhatDiem CN = new frmCapNhatDiem();
        int index = tbLop.getSelectedRow();
        TableModel m = tbLop.getModel();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn nhóm môn học muốn cập nhật điểm!", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            maMon = m.getValueAt(index, 1).toString();
            TenMon = m.getValueAt(index, 2).toString();
            Nhom = m.getValueAt(index, 3).toString();

            CN.setVisible(true);
            CN.setMaGV(maGV);
            CN.setTenNhom(Nhom);
            CN.setMaMon(maMon);
            CN.setTitle("Môn: " + TenMon + " - Nhóm: " + Nhom);
        }
    }//GEN-LAST:event_btnCapNhatActionPerformed

