    private void btnLietKeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLietKeActionPerformed
        // TODO add your handling code here:
        try {
            clsData.OpenConnect();
            PreparedStatement stm = clsData.con.prepareStatement("Select distinct G.MaMon, M.TenMon, G.Nhom\n"
                                                                    + "from GiangDay G join MonHoc M on G.MaMon = M.MaMon\n"
                                                                    + "where G.MaGV=? and G.NamHoc=? and G.HocKy=?");
            stm.setString(1, maGV);
            stm.setString(2, cboNamHoc.getSelectedItem().toString());
            stm.setString(3, cboHocKy.getSelectedItem().toString());
            ResultSet rs = stm.executeQuery();
            DefaultTableModel m = new DefaultTableModel(new Object[]{"STT", "Mã Môn", "Tên Môn", "Nhóm"}, 0);
            tbLop.setModel(m);
            int stt = 0;
            while (rs.next()) {
                stt++;
                ((DefaultTableModel) tbLop.getModel()).addRow(new Object[]{stt, rs.getString(1), rs.getString(2), rs.getString(3)});
            }
            clsData.CloseConnect();
        } catch (SQLException ex) {
            Logger.getLogger(frmMainGV.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnLietKeActionPerformed


